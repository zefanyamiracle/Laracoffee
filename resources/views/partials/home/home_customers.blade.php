            <h2> Jenis-jenis Kopi dan Cara Menikmatinya</h2>
            <p>Kopi hadir dalam berbagai jenis dan cara penyajian yang beragam. Dua jenis utama kopi yang paling banyak dikenal adalah Arabika dan Robusta. Biji kopi Arabika dikenal dengan rasa yang lebih halus dan asam, sementara Robusta memiliki rasa yang lebih kuat dan cenderung lebih pahit. Keduanya memiliki karakteristik yang berbeda, yang membuatnya cocok untuk berbagai cara penyajian.

              Jenis-jenis Kopi:
              Kopi Arabika: Dikenal dengan rasa yang lebih ringan, asam, dan kompleks. Biasanya lebih mahal karena proses penanamannya yang lebih rumit. Kopi Arabika sering digunakan untuk kopi spesialti.
              Kopi Robusta: Memiliki rasa yang lebih kuat, pahit, dan mengandung lebih banyak kafein. Robusta lebih murah dan sering digunakan dalam kopi instan atau campuran kopi.
              Metode Penyajian Kopi:
              Espresso: Penyajian kopi yang paling dasar dan kuat, menggunakan tekanan tinggi untuk mengektrak rasa dari biji kopi. Espresso menjadi dasar bagi berbagai minuman kopi lainnya, seperti cappuccino dan latte.
              Cappuccino: Kombinasi antara espresso, susu panas, dan busa susu, menciptakan rasa yang lembut dengan tekstur krimi.
              French Press: Metode penyeduhan kopi manual yang menghasilkan kopi dengan rasa yang lebih kaya dan bertekstur, berkat proses perendaman biji kopi dalam air panas selama beberapa menit.
              Pour-over: Metode penyeduhan manual di mana air panas dituangkan perlahan-lahan ke atas kopi yang terletak di filter, menghasilkan kopi dengan rasa yang lebih bersih dan kompleks.
              3. Manfaat Kopi bagi Kesehatan: Fakta atau Mitos?
              Kopi, selain dikenal sebagai minuman penggugah semangat, juga telah lama menjadi subjek penelitian mengenai manfaat kesehatannya. Berdasarkan berbagai studi ilmiah, kopi ternyata memiliki sejumlah manfaat yang baik bagi tubuh, meskipun harus dikonsumsi dengan bijak.
